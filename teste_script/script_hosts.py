# Vamos criar um programa que calcula a média de três números. 

# Primeiro, definimos os números.
n1 = float(input("Insira o primeiro número: "))
n2 = float(input("Insira o segundo número: "))
n3 = float(input("Insira o terceiro número: "))

# Calculamos a soma dos números.
soma = n1 + n2 + n3

# Calculamos a média.
media = soma / 3

print(f"A média dos três números é {media}")

